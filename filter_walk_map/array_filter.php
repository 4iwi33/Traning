<?php
// array_filter --  Применяет фильтр к массиву, используя функцию обратного вызова

// Функция array_filter() возвращает массив, содержащий значения массива исходный_массив
// отфильтрованные в соответствии с результатом функции обратного вызова. Если 
// исходный_массив является ассоциативным массивом, его ключи сохраняются.

function odd($var)
{
    return ($var % 2 == 1);
}

function even($var)
{
    return ($var % 2 == 0);
}

$array1 = array("a" => 1, "b" => 2, "c" => 3, "d" => 4, "e" => 5);
$array2 = array(6, 7, 8, 9, 10, 11, 12);

echo "Нечетные :\n";
print_r(array_filter($array1, "odd"));
echo "Четные   :\n";
print_r(array_filter($array2, "even"));
?>
