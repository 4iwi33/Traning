--Оператор "LIKE" выводим с указаным символом
-- '%' не важно какие дальше будут символы
SELECT *  --выводим всё
FROM test 
WHERE name 
LIKE 'J%' --символ который начинается с J...
------------------------------------------------------------------
SELECT *  --выводим всё
FROM test 
WHERE name 
LIKE '%o%'--символ который не важно счего
-- начинается и чем заканчивается, но чтоб была буква "о"
